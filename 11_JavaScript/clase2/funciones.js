
// Queremos una función que reciba dos números y los multiplique

// camelCase
// Crear la función y ponerle un nombre
function precioTotal (precioUnitario, cantidad) {
    var total = precioUnitario * cantidad;
    return total;
}

// Llamamos a la función y le entregamos los parámetros
console.log(precioTotal(15.09, 3));
alert(precioTotal(55, 23));


